SELECT NOW()
	,30
	,DW.SRC_ID
	,'DW_IMDB_BASE_TITLE_BASICS'
	,MAX(DW.LAST_UPDATE)
	,SUM(ROW_COUNT)
	,SUM(DISTINCT_PK_COUNT)
FROM (
	SELECT DW.TCONST_NK
		,DW.SRC_ID
		,MAX(DW.LAST_UPDATE) AS LAST_UPDATE
		,COUNT(*) AS ROW_COUNT
		,1 AS DISTINCT_PK_COUNT
	FROM DW_IMDB_BASE_TITLE_BASICS DW
	JOIN X_IMDB_BASE_TITLE_BASICS_CHG X
		ON DW.TCONST_NK = X.TCONST_NK
	WHERE DW.LAST_UPDATE = (
			SELECT MAX(LOAD_DATE)
			FROM X_IMDB_BASE_TITLE_BASICS_CHG
			)
	GROUP BY DW.TCONST_NK
		,DW.SRC_ID
	) AS DW
GROUP BY DW.SRC_ID;

