INSERT INTO DW_IMDB_BASE_TITLE_BASICS (
	P_TCONST_ID
	,TCONST_NK
	,TITLETYPE
	,PRIMARYTITLE
	,ORIGINALTITLE
	,ISADULT
	,STARTYEAR
	,ENDYEAR
	,RUNTIMEMINUTES
	,GENRES
	,SRC_ID
	,LOAD_DATE
	,LAST_UPDATE
	,NOTE
	)
SELECT M.MAX_ID + ROW_NUMBER() OVER (
		ORDER BY S.TCONST_NK
		)
	,S.TCONST_NK
	,S.TITLETYPE
	,S.PRIMARYTITLE
	,S.ORIGINALTITLE
	,S.ISADULT
	,S.STARTYEAR
	,S.ENDYEAR
	,S.RUNTIMEMINUTES
	,S.GENRES
	,S.SRC_ID
	,S.LOAD_DATE
	,S.LOAD_DATE
	,S.NOTE
FROM X_IMDB_BASE_TITLE_BASICS_CHG S
	,(
		SELECT MAX(P_TCONST_ID) MAX_ID
		FROM DW_IMDB_BASE_TITLE_BASICS
		) M
WHERE S.CHG_FLG = 'I';

/* UPDATE CHANGES */
UPDATE DW_IMDB_BASE_TITLE_BASICS
SET TCONST_NK = S.TCONST_NK
	,TITLETYPE = S.TITLETYPE
	,PRIMARYTITLE = S.PRIMARYTITLE
	,ORIGINALTITLE = S.ORIGINALTITLE
	,ISADULT = S.ISADULT
	,STARTYEAR = S.STARTYEAR
	,ENDYEAR = S.ENDYEAR
	,RUNTIMEMINUTES  = S.RUNTIMEMINUTES
	,GENRES = S.GENRES
	,SRC_ID = S.SRC_ID
	,LAST_UPDATE = S.LOAD_DATE
	,NOTE = S.NOTE
FROM X_IMDB_BASE_TITLE_BASICS_CHG S
WHERE S.TCONST_NK = DW_IMDB_BASE_TITLE_BASICS.TCONST_NK
	AND S.CHG_FLG = 'X';

