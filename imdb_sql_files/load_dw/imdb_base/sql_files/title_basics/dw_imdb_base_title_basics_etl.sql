/* DETECT CHANGES */
INSERT INTO X_IMDB_BASE_TITLE_BASICS_CHG (
	P_TCONST_ID
	,TCONST_NK
	,TITLETYPE
	,PRIMARYTITLE
	,ORIGINALTITLE
	,ISADULT
	,STARTYEAR
	,ENDYEAR
	,RUNTIMEMINUTES
	,GENRES
	,SRC_ID
	,LOAD_DATE
	,NOTE
	,CHG_FLG
	)
SELECT T.P_TCONST_ID
	,S.TITLETYPE
	,S.PRIMARYTITLE
	,S.ORIGINALTITLE
	,S.ISADULT
	,S.STARTYEAR
	,S.ENDYEAR
	,S.RUNTIMEMINUTES
	,S.GENRES
	,S.SRC_ID
	,S.LOAD_DATE
	,S.NOTE
	,CASE
		WHEN T.P_TCONST_ID IS NULL
			THEN 'I'
		ELSE 'X'
		END
FROM ST_IMDB_BASE_TITLE_BASICS S
LEFT OUTER JOIN DW_IMDB_BASE_TITLE_BASICS T
	ON T.TCONST_NK = S.TCONST_NK
WHERE T.P_TCONST_ID IS NULL
	OR T.PRIMARYTITLE <> S.PRIMARYTITLE
	OR T.ORIGINALTITLE <> S.ORIGINALTITLE
	OR T.ISADULT <> S.ISADULT
	OR T.STARTYEAR <> S.STARTYEAR
	OR T.ENDYEAR <> S.ENDYEAR
	OR T.RUNTIMEMINUTES <> S.RUNTIMEMINUTES
	OR T.GENRES <> S.GENRES
	OR T.SRC_ID <> S.SRC_ID
	OR T.NOTE <> S.NOTE;

ANALYZE X_IMDB_BASE_TITLE_BASICS_CHG;

/* SAVE CURRENT X_CHG LOAD LOG */
INSERT INTO X_IMDB_BASE_LOG (
	LOAD_TIMESTAMP
	,LOAD_SEQ_NUM
	,SRC_ID
	,OBJECT_NAME
	,LOAD_DATE
	,ROW_COUNT
	,DISTINCT_PK_COUNT
	)
SELECT NOW()
	,20
	,S.SRC_ID
	,'X_IMDB_BASE_TITLE_BASICS_CHG'
	,MAX(S.LOAD_DATE)
	,SUM(ROW_COUNT)
	,SUM(DISTINCT_PK_COUNT)
FROM (
